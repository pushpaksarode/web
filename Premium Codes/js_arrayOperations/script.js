let myArray1 = ["Abhay","Pushpak","Utkarsh","Devil"];
console.log(myArray1);

// this will return & remove last element in array

console.log(myArray1.pop());
console.log(myArray1);

// this will push element at last index of array

myArray1.push("Ritvik");
console.log(myArray1);

// this will return length of array

console.log(myArray1.length);  // length is Not a Function

//--------------------------------------------------------

// this will return & remove first element in array

console.log(myArray1.shift());
console.log(myArray1);

// this will push element at first index of array

myArray1.unshift("Alian");
console.log(myArray1);

